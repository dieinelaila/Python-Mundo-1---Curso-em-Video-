numero = int(input("Digite um número inteiro: "))

# Calcular o sucessor e o antecessor
sucessor = numero + 1
antecessor = numero - 1

# Mostrar o resultado
print(f"O sucessor de {numero} é {sucessor}.")
print(f"O antecessor de {numero} é {antecessor}.")

print('O sucessor de {} é {}'.format(numero,sucessor))
print('O antecessor de {} é {}'. format(numero, antecessor))

